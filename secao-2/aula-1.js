// Operadores de comparação

let valor1 = 20;
let valor2 = "20";
let valor3 = 10;
let valor4 = "9";

console.log("== " + (valor1 == valor2)); // valor é igual?
console.log("=== " + (valor1 === valor2)); // valor e tipo são iguais ?
console.log("!= " + (valor1 != valor2)); // valor é diferente ?
console.log("!== " + (valor1 !== valor2)); // valor e tipo são diferentes ?
console.log("> " + (valor3 > valor4));
console.log("< " + (valor3 < valor4));
console.log(">= " + (valor3 >= valor4));
console.log("<= " + (valor3 <= valor4));

