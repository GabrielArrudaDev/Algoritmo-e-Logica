// Operadores lógicos

let freteGratis = true;
let estoque = false;

console.log(freteGratis && estoque ); // Se um for verdadeiro e um falso, será falso
console.log(freteGratis || estoque ); // Se um for verdadeiro e um falso, sera verdadeiro
console.log(!freteGratis); // Nega (inverte)

let valor = 21391239;
console.log("valor " + (!!valor)); // Usada para converter um valor para seu equivalente booleano (0 = false / 1+ = true)

